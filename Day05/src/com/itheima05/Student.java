package com.itheima05;

public class Student {
	private String name;
	private int age;
	private int sss;
	private String iii;
	
	public int getSss() {
		return sss;
	}
	public void setSss(int sss) {
		this.sss = sss;
	}
	public String getIii() {
		return iii;
	}
	public void setIii(String iii) {
		this.iii = iii;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getName(){
		return name;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getAge() {
		return age;
	}

}
