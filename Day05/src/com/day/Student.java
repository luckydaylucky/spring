package com.day;
//学生类
public class Student {

//	 成员变量
	  private int age;
	  private String name;
//	  构造方法
	public Student() {
	}
	public Student(int age, String name) {
		this.age = age;
		this.name = name;
	}
//	成员方法
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
		
		
	}
