package com.itheima;
/*关系运算符*/
public class OperaterDemo_05 {
	public static void main(String[] args) {
//		定义变量
		int a = 100;
		int b = 200;
//		输出关系运算符的结果
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a>b);
		System.out.println(a<b);
		
	}

}
