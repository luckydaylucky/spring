package com.itheima;

public class IfDemo {
	public static void main(String[] args) {
		System.out.println("开始");
		//定义int型变量
		int a = 10;
		int b = 20;
		
		//判断两个变量是否相等
		if (a == b) {
			System.out.println("a等于b");
		}
		//定义变量
		int c = 20;
		if (b == c) {
			System.out.println("b等于c");
		}
		
		System.out.println("结束");
	}
}
